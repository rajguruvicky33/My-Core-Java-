package com.exp.in1;

public class ComparisonOperator {
	public static void main(String[] args) {
		int a=5;
		int b=8;
		
		if(a==b) {
			System.out.println("value a=value b");
		}
		if (a!=b) {
			System.out.println("Value of a is not equal to b");
		}
		if (a<=b) {
			System.out.println("Value of a is less than b");
		}
	}

}
