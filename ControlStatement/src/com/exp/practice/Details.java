package com.exp.practice;

public class Details {
	int jerseyId;
	String playerName;
	String state;
	int pinCode;
	long contactNumber;
	public Details(int jerseyId, String playerName, String state, int pinCode, long contactNumber) {
		
		this.jerseyId = jerseyId;
		this.playerName = playerName;
		this.state = state;
		this.pinCode = pinCode;
		this.contactNumber = contactNumber;
	}
}
